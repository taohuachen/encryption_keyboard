void DisplayTaskFunc(void const *argument)
{
    LCD_Init();
    LCD_SetBrightness(100);
    GUI_Init();

    int i = 0;
    GUI_DispString("Hello world!");

    for (;;)
    {
        printf("DisplayTask is running!\n");

        GUI_DispDecAt(i++, 20, 20, 4);
        if (i > 9999)
        {
            i = 0;
        }

        LCD_Display();

        osDelay(50);
    }
}
