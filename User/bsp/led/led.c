/* Includes ------------------------------------------------------------------*/
#include "led.h"
#include "main.h"

/* Private typedef -----------------------------------------------------------*/
/* Private define ------------------------------------------------------------*/
/* Private macro -------------------------------------------------------------*/
/* Private variables ---------------------------------------------------------*/
IO_TypeDef led = {LED_GPIO_Port, LED_Pin};

/* Private function prototypes -----------------------------------------------*/
/* Private functions ---------------------------------------------------------*/
/* Public functions ----------------------------------------------------------*/
/**
 * @brief LED打开
 */
void LED_On(void)
{
    IO_WritePin(&led, 0);
}

/**
 * @brief LED关闭
 */
void LED_Off(void)
{
    IO_WritePin(&led, 1);
}

/**
 * @brief LED取反
 */
void LED_Toggle(void)
{
    IO_TogglePin(&led);
}
